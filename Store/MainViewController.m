- (void)initializeGame
{
	// Play the new game sound
	[self playSound:newId];
    
	// Initialize the game
    MainView *mainView = (MainView *)self.view;
    self.fGameOver = NO;
    mainView.ulMoves = 0;
    NSUserDefaults *defaults = [NSUserDefaults standardUserDefaults];
    if (self.fNextLevel) {
        mainView.sLevel++;
        [defaults setInteger:mainView.sLevel forKey:kNewLevelKey];
        [defaults setInteger:mainView.sLevel forKey:kLevelKey];
        self.fNextLevel = NO;
    } else {
        short sNewLevel = [defaults integerForKey:kNewLevelKey];
        mainView.sLevel = sNewLevel;
        [defaults setInteger:mainView.sLevel forKey:kLevelKey];
    }
    [defaults synchronize];
    
    self.ptlWorker = ptlWorkerInitial[mainView.sLevel];
    self.undoButton.hidden = NO;

	// initialize grid structure
    Board boardIni;
	for(short x = 0; x < COLUMNSX; x++)
		for(short y = 0; y < LINESY; y++)
			boardIni.location[x][y] = BoardLevels[mainView.sLevel][x][y];
    mainView.board = boardIni;
    UIInterfaceOrientation orientation = [[UIApplication sharedApplication] statusBarOrientation];
    if (UIInterfaceOrientationIsLandscape(orientation)) {
        switch (mainView.board.location[ptlWorker.x][ptlWorker.y])
        {
            case 9: // Up
                self.sLastDir = 1;
                break;
            case 11: // Down
                self.sLastDir = 3;
                break;
            case 8: // Left
                self.sLastDir = 0;
                break;
            case 10: // Right
                self.sLastDir = 2;
                break;
        }
    } else {
        switch (mainView.board.location[ptlWorker.x][ptlWorker.y])
        {
            case 9: // Up
                self.sLastDir = 1;
                break;
            case 11: // Down
                self.sLastDir = 3;
                break;
            case 8: // Left
                self.sLastDir = 0;
                break;
            case 10: // Right
                self.sLastDir = 2;
                break;
        }
    }
    mainView.text = [[NSString alloc] initWithFormat: @"Swipe to change direction. Tap to move forward."];
    
	// Draw the view
	[mainView setNeedsDisplay];
}

